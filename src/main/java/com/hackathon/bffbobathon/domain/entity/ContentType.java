package com.hackathon.bffbobathon.domain.entity;

/**
 * Enum que representa os tipos de conteúdo disponíveis no sistema.
 */
public enum ContentType {
    /**
     * Conteúdo em formato de vídeo.
     */
    VIDEO,

    /**
     * Conteúdo em formato de artigo/texto.
     */
    ARTICLE,

    /**
     * Conteúdo em formato de tutorial passo a passo.
     */
    TUTORIAL,

    /**
     * Conteúdo em formato de documentação técnica.
     */
    DOCUMENTATION,

    /**
     * Conteúdo em formato de curso completo.
     */
    COURSE,

    /**
     * Conteúdo em formato de podcast/áudio.
     */
    PODCAST
}

